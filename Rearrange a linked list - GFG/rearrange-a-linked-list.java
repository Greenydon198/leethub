class Solution
{

   // Should rearrange given linked list such that all even
   // positioned Nodes are before odd positioned.
   // Returns nothing
    void rearrangeEvenOdd(Node head)
    {
          //  The task is to complete this method
          Node o = head;
          Node e = head.next;
          Node ans = e;
          while(e!=null && e.next!=null){
              o.next=o.next.next;
              e.next=e.next.next;
              o=o.next;
              e=e.next;
          }
          o.next=ans;
     }
}
